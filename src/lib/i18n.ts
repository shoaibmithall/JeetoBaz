export type TranslationKey = keyof typeof translations.en;

function normalizeLanguage(value: string | null): LanguageCode {
  return value === 'ur' || value === 'roman' ? value : 'en';
}

export function translate(language: LanguageCode, key: TranslationKey) {
  return translations[language][key] || translations.en[key];
}

export async function setAppLanguage(language: LanguageCode) {
  await setStoredValue(LANGUAGE_KEY, language);
  listeners.forEach((listener) => listener(language));
}

export function useLanguage() {
  const [language, setLanguageState] = useState<LanguageCode>('en');

  useEffect(() => {
    let active = true;
    getStoredValue(LANGUAGE_KEY).then((value) => {
      if (active) setLanguageState(normalizeLanguage(value));
    });
    const listener = (nextLanguage: LanguageCode) => setLanguageState(nextLanguage);
    listeners.add(listener);
    return () => {
      active = false;
      listeners.delete(listener);
    };
  }, []);

  return {
    language,
    t: (key: TranslationKey) => translate(language, key),
    setLanguage: setAppLanguage,
  };
}
